CREATE TABLE IF NOT EXISTS ORGANIZATION_HIERARCHY (
ORG_ID UUID DEFAULT GEN_RANDOM_UUID() PRIMARY KEY,
PARENT_ORG_ID  UUID,
ORG_TYPE_ID    UUID,
ORG_NAME       VARCHAR(250),
COO_OWNER      VARCHAR(500),
OPS_LEAD       VARCHAR(500),
TECH_LEAD      VARCHAR(500),
BUS_OWNER      VARCHAR(500),
ORG_DESC       VARCHAR(4000),
ORG_EMAIL      VARCHAR(500),
ORG_CI         VARCHAR(500),
USER_FIELD_1   VARCHAR(500),
USER_FIELD_2   VARCHAR(500),
USER_FIELD_3   VARCHAR(500),
USER_FIELD_4   VARCHAR(500),
USER_FIELD_5   VARCHAR(500),
STATUS         VARCHAR(1)             DEFAULT 'A',
INSERTED_USER  VARCHAR(500)           DEFAULT CURRENT_USER ,
INSERT_DT      DATE                   DEFAULT CURRENT_TIMESTAMP ,
MODIFIED_BY    VARCHAR(500),
UPDATE_DT      DATE,
FOREIGN KEY (ORG_TYPE_ID) REFERENCES ORGANIZATION_TYPES (ORG_TYPE_ID) ON DELETE CASCADE
);